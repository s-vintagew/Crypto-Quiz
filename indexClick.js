function mclick() {
    var items = document.getElementsByClassName('m');
    for (var i = 0; i < items.length; i++) {
        items[i].style.background = "yellow";
    }
}

function yclick() {
    var items = document.getElementsByClassName('y');
    for (var i = 0; i < items.length; i++) {
        items[i].style.background = "yellow";
    }
}